# Se va a crear un bucle for que imprime los numeros hasta el 10
print("Count to 10!")

# Se va a crear una variable x que va a ir de 0 hasta 10 
# Dato importante, no se cuenta el ultimo valor del range, en este caso, en vez del 0 al 11, la realidad es que es del 0 al 10
for x in range(0, 11):
    print(x)